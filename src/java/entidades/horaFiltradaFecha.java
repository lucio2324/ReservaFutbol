package entidades;


public class horaFiltradaFecha {
    private String nombre_cancha;
    private String hora_hora;
    private String disponible;


    public String getNombre_cancha() {
        return nombre_cancha;
    }


    public void setNombre_cancha(String nombre_cancha) {
        this.nombre_cancha = nombre_cancha;
    }


    public String getHora_hora() {
        return hora_hora;
    }


    public void setHora_hora(String hora_hora) {
        this.hora_hora = hora_hora;
    }


    public String getDisponible() {
        return disponible;
    }


    public void setDisponible(String disponible) {
        this.disponible = disponible;
    }

}
